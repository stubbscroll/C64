; algorithm: traverse the "tree" by using a custom stack to keep track of
; number of remaining children and number of metadata for all parent nodes
; runtime: 1 second

	CHROUT = $ffd2

	!to "08a.prg",cbm
	* = $0801
	; sys start
	!byte $0b, $08, $0a, $00, $9e, 48+start/1000%10, 48+start/100%10, 48+start/10%10, 48+start%10, $00, $00, $00

	zp = $fe
	sp = $fc ; stack pointer
	curn = $fa ; number of children in current node
	curm = $fb ; number of metadata in current node

	answer = $f7 ; 3 bytes

start	sei
	ldx #<stack
	ldy #>stack
	stx sp+0
	sty sp+1
	ldx #<input
	ldy #>input
	stx zp+0
	sty zp+1
	lda #0
	sta answer+0
	sta answer+1
	sta answer+2
loop2	ldy #0
	lda (zp),y
	sta (sp),y
	iny
	lda (zp),y
	sta (sp),y
	jsr inczp
	jsr inczp
loop	; check if sp < stack
	lda sp+0
	cmp #<stack
	lda sp+1
	sbc #>stack
	bcc done
	ldy #0
	lda (sp),y ; read number of children left to process
	beq child0 ; none left
	; push next child
	sec
	sbc #1
	sta (sp),y ; reduce number of children by 1 on stack
	jsr incsp
	jsr incsp
	jmp loop2
child0	iny
	; read metadata
	lda (sp),y
	tax
	ldy #0
-	lda (zp),y
	; add to answer
	clc
	adc answer+0
	sta answer+0
	bcc +
	inc answer+1
	bne +
	inc answer+2
+	jsr inczp
	dex
	bne -
	jsr decsp
	jsr decsp
	jmp loop
done	; output answer
	ldx #2
-	lda answer,x
	sta inbcd,x
	dex
	bpl -
	jsr int24tobcd
	lda #4
	ldx #<outbcd
	ldy #>outbcd
	jsr printbcd
	lda #$0d
	jmp CHROUT

inczp	inc zp+0
	bne +
	inc zp+1
+	rts

incsp	inc sp+0
	bne +
	inc sp+1
+	rts

decsp	lda sp+0
	bne +
	dec sp+1
+	dec sp+0
	rts

; convert unsigned 24-bit int to 32-bit (8-digit) bcd
; input value: inbcd
; output value: outbcd
; clobbered: a,x
